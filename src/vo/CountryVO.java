package vo;

public class CountryVO {
	private int coun_num;
	private String coun_name;
	private String count_time;
	
	public int getCoun_num() {
		return coun_num;
	}
	public void setCoun_num(int coun_num) {
		this.coun_num = coun_num;
	}
	public String getCoun_name() {
		return coun_name;
	}
	public void setCoun_name(String coun_name) {
		this.coun_name = coun_name;
	}
	public String getCount_time() {
		return count_time;
	}
	public void setCount_time(String count_time) {
		this.count_time = count_time;
	}
	
}
