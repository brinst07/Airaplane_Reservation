package vo;

public class SitVO {
	
	private int sit_num;
	private int sit_number;
	private String sit_class;
	private int tick_num;
	public int getSit_num() {
		return sit_num;
	}
	public void setSit_num(int sit_num) {
		this.sit_num = sit_num;
	}
	public int getSit_number() {
		return sit_number;
	}
	public void setSit_number(int sit_number) {
		this.sit_number = sit_number;
	}
	public String getSit_class() {
		return sit_class;
	}
	public void setSit_class(String sit_class) {
		this.sit_class = sit_class;
	}
	public int getTick_num() {
		return tick_num;
	}
	public void setTick_num(int tick_num) {
		this.tick_num = tick_num;
	}
	
	
}
